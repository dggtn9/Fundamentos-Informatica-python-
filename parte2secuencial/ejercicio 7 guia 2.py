"""Una persona invierte su capital en un banco y desea saber cuánto dinero
ganará en un mes, teniendo en cuenta que el banco paga 2% mensual.
¿Cuánto ganará en seis meses si deja su dinero invertido?"""
interes=2
capital=int(input("Ingrese su capital a invertir"))
ganancia_por_mes=(capital*interes)/100
print("En un mes ganaras:" + str(ganancia_por_mes))
ganancia_seis_meses=ganancia_por_mes*6
print("En seis meses ganaras:" + str(ganancia_seis_meses))

                         





